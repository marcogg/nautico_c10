					<h1 class="text-center">AVISO DE PRIVACIDAD</h1>
              <p>
En cumplimiento con lo establecido por la Ley Federal de Protección de Datos Personales en Posesión de Particulares publicada en el Diario Oficial de la Federación el día 5 de Julio de 2010, así como su correspondiente Reglamento (en conjunto la “Ley”) INMOBILIARIA SULIN S.A. DE C.V., designando como domicilio para los efectos del presente el ubicado en ALFONSO REYES 173, COL. CONDESA, DEL. CUAUHTEMOC, C.P. 06140 MÉXICO D.F. extiende y hace de su conocimiento para todos los fines legales a que haya lugar, el presente Aviso de Privacidad de Datos Personales.<BR>

1.	¿Que son los Datos Personales?
Es información concerniente a una persona física identificada o identificable, expresada en forma numérica, alfabética, gráfica, fotográfica, acústica o de cualquier otro tipo, la cual puede comprender de forma enunciativa: (i) datos generales como nombre completo, domicilio, teléfonos fijos y móviles, domicilio fiscal, Registro Federal de Contribuyentes (RFC), Clave Única de Registro de Población (CURP), dirección de correo electrónico (e-mail), ocupación, experiencia profesional, referencias personales de terceros; (ii) datos de índole patrimonial y/o financiero como datos de cuentas bancarias y número de Clabe Interbancaria. En lo sucesivo los datos previamente citados serán denominados, en su conjunto, como los “Datos Personales”.<br>

Para los fines que más adelante se señalan, Inmobiliaria Sulin podrá solicitarle su nombre completo, empresa para la que trabaja y su puesto, y que además le exhiba una identificación oficial con fotografía que le será devuelta al momento de registrar su salida de nuestras instalaciones.<br>

II ¿Para que utilizamos sus Datos Personales?
Inmobiliaria Sulin recabará los Datos Personales que usted libre y voluntariamente le proporcione, única y exclusivamente con el fin de poderlo identificar, y permitirle el acceso a nuestra Página Web y en su caso, poner a su disposición nuestros productos, para cuyo acceso nuestro portal podrá implementar medidas de seguridad.<br>

Es importante que usted tenga en cuenta que de no contar con sus Datos Personales no estaríamos en posibilidad de llevar a cabo los fines para los cuales se requieren y en tal caso no tendríamos ningún tipo de responsabilidad para la debida consecución de los mismos.<br>

Una vez que se cumpla la finalidad del tratamiento de Datos Personales, éstos serán bloqueados con el único propósito de determinar posibles responsabilidades en relación con su tratamiento, hasta el plazo de prescripción legal o contractual de éstas. Durante dicho periodo, los Datos Personales no podrán ser objeto de tratamiento y transcurrido éste, se procederá a su cancelación en la Base de Datos que corresponde.<br>

III. ¿Cómo recolectamos los Datos Personales?
La recolección de Datos Personales se efectúa cumpliendo todos los principios que marca la Ley, como la licitud, calidad, consentimiento, información, finalidad, lealtad, proporcionalidad y responsabilidad.
Los Datos Personales serán recabados directamente del titular y mediante el registro de los mismos en nuestro portal de internet.<br>

Además para fines de verificación y consulta, así como corroboración de la veracidad y exactitud de los Datos Personales que usted nos proporciona, podremos obtener información de otras fuentes permitidas por la ley, tales como directorios telefónicos, referencias de otras empresas o particulares, bases de datos públicas de cualquier entidad o dependencia pública o privada, entre otras.<br>

1.	¿Con quién compartimos sus Datos Personales?
Inmobiliaria Sulin podrá transferir sus Datos Personales únicamente a sus subsidiarias y afiliadas a efectos de cumplir con sus procesos administrativos de registro de visitantes. Inmobiliaria Sulin podrá revelar o permitir el acceso a los Datos Personales que usted nos proporcione, para cumplir con la legislación aplicable o por requerimiento de la autoridad competente. En ningún caso comercializaremos, venderemos o rentaremos información personal sobre usted a un tercero sin un consentimiento previo de su parte.<br>

Los Datos Personales que usted nos proporcione, podrán compilarse y fijarse en una base de datos propiedad exclusiva de Inmobiliaria Sulin.
Entendemos que ha otorgado su consentimiento a la transferencia de sus Datos Personales, al no haber manifestado oposición a través de los medios que hemos puesto a su disposición y hecho de su conocimiento en términos de este Aviso.<br>

1.	¿Cómo protegemos sus Datos Personales?
Los Datos Personales serán guardados bajo la más estricta confidencialidad y no se les podrá dar un uso distinto a los antes mencionados.<br>

En Inmobiliaria Sulin se cuenta con las medidas de seguridad, administrativas, técnicas y físicas necesarias y suficientes para proteger sus datos personales contra daño, pérdida, alteración, destrucción, uso, acceso o tratamiento no autorizado, mismas que exigimos sean cumplidas igualmente por nuestros prestadores o proveedores de servicios aun y cuando éstos sean subsidiarias o afiliadas de   Inmobiliaria Sulin.<br>

1.	¿Quién es el encargado o área responsable del tratamiento de los Datos Personales?
El área responsable del tratamiento de Datos Personales en Inmobiliaria Sulin es el Departamento Jurídico, con domicilio en ALFONSO REYES 173, COL. CONDESA, DEL. CUAUHTEMOC, C.P. 06140 MÉXICO D.F., horarios de atención de LUNES A VIERNES DE 09:00 A 19:00 HRS. de Lunes a Viernes, correo electrónico privacidad@grupohodaya.com, teléfono 55-5272.4305.<br>

VII. ¿Cómo puede ejercer sus derechos de acceso, rectificación, cancelación u oposición, de conformidad con lo dispuesto en esta Ley?
Usted podrá ejercer sus derechos ARCO (acceso, rectificación, cancelación y/u oposición) contactando directamente al área responsable del tratamiento de Datos Personales vía correo electrónico privacidad@grupohodaya.com La solicitud ARCO la podrá realizar mediante el envío de un escrito libre el cual deberá contener por lo menos: (i) nombre y domicilio del titular u otro medio para comunicarle la respuesta a su solicitud (ii) documentos que acrediten su identidad o, en su caso, la representación legal del titular (iii) descripción clara y precisa de los Datos Personales respecto de los que se busca ejercer alguno de los derechos (iv) cualquier otro elemento o documento que facilite la localización de los datos personales, así como cualquier otro documento que exija la legislación vigente al momento de la presentación de la solicitud.<br>

Para tener mayor información respecto al ejercicio de sus derechos ARCO puede comunicarse directamente con el Encargado del Tratamiento de Datos Personales cuyos datos aparecen en el apartado VI del presente Aviso de Privacidad.<br>

VIII. ¿Cuándo puede usted ejercer sus derechos ARCO?
En términos de la Ley y el Reglamento, usted podrá presentarnos a partir de este momento una solicitud para ejercer sus derechos ARCO; y dentro de los 15 días siguientes a la fecha en que Inmobiliaria Sulin dé respuesta a su solicitud o, si transcurrido el término que señala la Ley, Inmobiliaria Sulin no diera respuesta a su solicitud, entonces podrá iniciar el procedimiento de protección de derechos ARCO ante el Instituto Nacional de transparencia y Acceso a la Información y Protección de Datos Personales (INAI).<br>

1.	¿Cómo puede revocar su consentimiento para el tratamiento de Datos Personales?
Usted podrá revocar su consentimiento para el tratamiento de sus Datos Personales en cualquier momento, debiendo, únicamente para tales efectos, enviar una solicitud por escrito al área responsable de la protección de los Datos Personales, ya sea por mensajería al domicilio o al correo electrónico mencionados en el apartado VI del presente Aviso.<br>

Tendremos un plazo máximo de 15 días hábiles para informarle sobre la procedencia de la misma y si resulta procedente, se haga efectiva la misma dentro de los quince días hábiles siguientes a la fecha en que se comunica la respuesta al correo electrónico que nos proporcione para tales efectos.
1.	Cambios al Aviso de Privacidad.<br>

Nos reservamos el derecho de cambiar este Aviso de Privacidad en cualquier momento. En caso de que exista algún cambio en este Aviso de Privacidad, se les comunicará mediante página de internet www.grupoSulin.com, vía correo electrónico o en el domicilio registrado que nos proporcione. No seremos responsables si usted no recibe la notificación sobre el cambio a este Aviso de Privacidad, si existiere algún problema con su cuenta de correo electrónico, de transmisión de datos por internet, o por cualquier causa no imputable a Inmobiliaria Sulin. Por su seguridad, revise en todo momento que así lo desee el contenido de este Aviso de Privacidad enviando una solicitud a nuestra cuenta de correo electrónico: privacidad@grupohodaya.com<br>

1.	Otras normatividades aplicables.
Las leyes y regulaciones de otros países pueden imponer diferentes requerimientos para la protección de la información en general y de los datos personales que se recolectan vía internet o por cualquier otro medio. Inmobiliaria Sulin se encuentra ubicado en México y todos los asuntos en relación a este Aviso son regidos por las leyes de México. Si Usted está ubicado en algún otro país distinto de México y nos contacta, por favor tome en cuenta que cualquier información que Usted nos provea será transferida a México, y al momento de ingresar su información Usted autoriza esta transferencia y la aceptación del presente Aviso de Privacidad.<br>

